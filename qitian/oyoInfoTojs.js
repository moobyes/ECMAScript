function handlePriceParams(oyoInfos, roomPriceInfo){
  const changePriceList = oyoInfos.map(item => {
    let roomPriceInfoList = [] // 定义房间价格信息数组
    let dateRangeInfo = [] // 定义
    let roomPriceInfoListObj = {}
    let dateRangeInfoObj = {}
    roomPriceInfo.forEach(v => {
      if(+item.otaRoomTypeId === v.roomTypeID && new Date(item.endDate).Format('yyyy-MM-dd') === v.effectDate) {
        roomPriceInfoListObj = {
          roomTypeID: v.roomTypeID,
          // roomName: `(ID:${v.roomTypeID})${v.hotelName}`,
          // hotelID: v.hotelID,
          payType: v.payType,
          currency: v.currency,
          mealNum:'-100',
          weekDayIndex:'1111111',
          costPrice: item.preSellPrice,
          salePrice: item.postSellPrice,
          commissionRate: v.commissionRate,
          commissionValue: +(item.postSellPrice*v.commissionRate).toFixed(1),
          serviceFeeRate:0,
          refRoomIDs:[]
        }
        dateRangeInfoObj = {
          startDate: new Date(item.beginDate).Format('yyyy-MM-dd'),
          endDate: new Date(item.endDate).Format('yyyy-MM-dd')
        }
      }
      
    })
    roomPriceInfoList.push(roomPriceInfoListObj)
    dateRangeInfo.push(dateRangeInfoObj)
    return {
      roomPriceInfoList,
      dateRangeInfo,
      pageType: 'T',
      priceChangeModel: 1
    }
  })
  return changePriceList;
}
